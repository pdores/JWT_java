package com.nn.jwt_java;

public class ConfigFileUpdates {
    private int id;
    private int type;
    private String name;
    private int version;
    private int size;
    private String hash;

    public int getId() {
        return id;
    }

    public int getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public int getVersion() {
        return version;
    }

    public int getSize() {
        return size;
    }

    public String getHash() {
        return hash;
    }
}
